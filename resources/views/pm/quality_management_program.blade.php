                <h1>Six Sigma Methodologies </h1>
                <p>There are mainly two methodologies of Six Sigma namely&nbsp;<strong>DMAIC</strong>&nbsp;and&nbsp;<strong>DMADV</strong>.</p>
                <p><strong>DMAIC</strong>&nbsp;is a data-driven Six Sigma methodology for improving existing products and processes. The&nbsp;<strong>DMAIC</strong>&nbsp;process should be used when an existing product or process can be improved to meet or exceed the customer&rsquo;s requirements.&nbsp;<strong>DMAIC</strong>&nbsp;methodology consists of five phases: D – Define, M – Measure, A – Analyze, I – Improve, C – Control.</p>
                <ul>
                    <li>Define – Define the project targets and customer (internal and external) deliverables.</li>
                    <li>Measure – Measure the process to determine the current process performance (baseline).</li>
                    <li>Analyze – Find out the root causes of the defects.</li>
                    <li>Improve – Improve the process by eliminating defects.</li>
                    <li>Control – Control the future performance.</li>
                </ul>
                <hr style="height:1px; background-color:#eee; border:none; margin:1em 0" />
                <p><strong>DMADV</strong>&nbsp;is a common DFSS (Design for Six Sigma) methodology used to develop a process or product which does not exist in the company. DFSS is an application of Six Sigma which focuses on the design or redesigns of the different processes used in product manufacturing or service delivery by taking into account the customer needs and expectations.&nbsp;<strong>DMADV</strong>&nbsp;is used when the existing product or process does not meet the level of customer specification or Six Sigma level even after optimization with or without using&nbsp;<strong>DMAIC</strong>.&nbsp;<strong>DMAIC</strong>&nbsp;methodology consists of five phases: D – Define, M – Measure, A – Analyze, D – Design, V – Validate.</p>
                <ul>
                    <li>DEFINE the project goals and customer deliverables</li>
                    <li>MEASURE the process to determine the current performance level</li>
                    <li>ANALYZE and determine the root causes of the defects</li>
                    <li>DESIGN the process in detail to meet customer needs</li>
                    <li>VALIDATE the design performance and its ability to meet the customer needs </li>
                </ul>
            </div>
